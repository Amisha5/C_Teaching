#include<stdio.h>
void main()
{
	//Escape Sequence
	printf("Name : xyz \nAddress : xyz\n"); // \n used for line break
	printf("Name : \" xyz \"\n"); // \" is used to print name inside this(" ")
	printf("Name : \' xyz \'\n"); // \' used to print name inside this('')
	printf("name : \t xyz"); // \t is used for space
}
